     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'email' => 'required',
            'phone' => 'required',
            'date' => 'required',
            'time' => 'required',
            'people' => 'required',
            'message' => 'required'
        ]);
        // dd($request);
        $input = $request->all();

        $contact = Contact::create($input);
        // dd($contact);
        
        return back()->with('success', 'Your booking request was sent. We will call back or send an Email to confirm your
        reservation. Thank you!');
    }

